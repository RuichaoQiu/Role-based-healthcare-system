package edu.ncsu.csc.itrust.beans;

public class PregnancyBean {
	private int year = 2000;
	private int weeksPreg = 0;
	private int daysPreg = 0;
	private double laborHours = 0.0;
	private String deliveryType = "";

	public PregnancyBean() {
	}
	
	public void setYearConceived(String str) {
		year = Integer.parseInt(str);
	}
	
	public void setWeeksPreg(String str) {
		weeksPreg = Integer.parseInt(str);
	}
	
	public void setDaysPreg(String str) {
		daysPreg = Integer.parseInt(str);
	}
	
	public void setLaborHours(String str) {
		laborHours = Double.parseDouble(str);
	}
	
	public void setDeliveryType(String str) {
		deliveryType = str;
	}
	
	public String getYearConceived() {
		return "" + year;
	}
	
	public String getWeeksPreg() {
		return "" + weeksPreg;
	}
	
	public String getDaysPreg() {
		return "" + daysPreg;
	}
	
	public String getLaborHours() {
		return "" + laborHours;
	}
	
	public String getDeliveryType() {
		return deliveryType;
	}
}
